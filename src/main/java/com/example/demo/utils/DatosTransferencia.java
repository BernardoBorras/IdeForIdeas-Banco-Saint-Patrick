package com.example.demo.utils;

public class DatosTransferencia {

    private String debitadoDe;
    private String acreditadoA;
    private Float monto;

    public String getDebitadoDe() {
        return debitadoDe;
    }

    public void setDebitadoDe(String debitadoDe) {
        this.debitadoDe = debitadoDe;
    }

    public String getAcreditadoA() {
        return acreditadoA;
    }

    public void setAcreditadoA(String acreditadoA) {
        this.acreditadoA = acreditadoA;
    }

    public Float getMonto() {
        return monto;
    }

    public void setMonto(Float monto) {
        this.monto = monto;
    }

}
